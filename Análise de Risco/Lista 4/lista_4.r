funcCpm <- function(n, d, Suc, Pre) {
  est <- eft <- lst <- lft <- numeric(n)

  # Forward pass
  cpmf <- function(s, est) {
    eft[s] <<- est[s] + d[s]
    if (Suc[[s]][1] != 0)
      for (i in Suc[[s]]) {
        if (est[i] < eft[s]) est[i] <- eft[s]
        est <- cpmf(i, est)
      }
    est
  }

  # Backward pass
  cpmb <- function(s, lft) {
    lst[s] <<- lft[s] - d[s]
    if (Pre[[s]][1] != 0)
      for (i in Pre[[s]]) {
        if (lft[i] > lst[s]) lft[i] <- lst[s]
        lft <- cpmb(i, lft)
      }
    lft
  }

  rf  <- cpmf(1, est)
  lf  <- rep(rf[n] + d[n], n)
  rb  <- cpmb(n, lf) - d
  list(est = rf, lst = rb, slack = rb - rf)
}

