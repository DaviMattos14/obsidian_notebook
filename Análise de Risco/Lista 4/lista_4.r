rtri <- function(n, a, c, b) {
  # a=min, b=max, c=modo
  if (b == a) return(rep(a, n))
  u  <- runif(n)
  fc <- (c - a) / (b - a)
  ifelse(u < fc,
    a + sqrt(u  * (b - a) * (c - a)),
    b - sqrt((1 - u) * (b - a) * (b - c))
  )
}

