funcCpm <- function(n=9,
                    d=c(0,2,6,4,3,5,4,2,0),
                    Suc=list(c(2,3,4),c(5,7),8,6,8,8,9,9,0),
                    Pre=list(0,1,1,1,2,4,2,c(3,5,6),c(7,8)))
{
  est <- rep(0, n)
  eft <- rep(0, n)
  lst <- rep(0, n)
  lft <- rep(0, n)

  # ---------- FORWARD ----------
  for(i in 1:n){
    if(Pre[[i]][1] != 0){
      est[i] <- max(eft[Pre[[i]]])
    }
    eft[i] <- est[i] + d[i]
  }

  # ---------- BACKWARD ----------
  lft[n] <- eft[n]
  lst[n] <- lft[n] - d[n]

  for(i in (n-1):1){
    if(Suc[[i]][1] != 0){
      lft[i] <- min(lst[Suc[[i]]])
    } else {
      lft[i] <- eft[n]
    }
    lst[i] <- lft[i] - d[i]
  }

  slack <- lst - est

  list(est=est, lst=lst, slack=slack, duracao=eft[n])
}

